import loadingComponent from "./components/loading/loading-component";
import API from "./services/landAPI";
import questionsList from "./components/questions-list/questions-list";

export default {
  name: "app",
  data: function() {
    return {
      currentTime: null,
      currentTimestart: null,
      curentdate: new Date(),
      timer: null,
      countryCode: "07",
      phone: "",
      progress: 20,
      totalTime: 3 * 60,
      buttonNext: true,
      phoneNumber: null,
      phoneMask: "0799-999-999",
      loadingModal: false,
      showLastPage: false,
      view: 0,
      key: "",
      QUERY: this.getQuery(window.location.search),
      click_id: null,
      phoneInfo: null,
      smsFlow: null,
      messageText: this.checkId(),
      buttonLink: null,
      buttonTtext: null,
      resultReady: false,
      seller: false
    };
  },
  components: {
    loadingComponent,
    questionsList
  },
  methods: {
    maskCheck: function(field) {
      if (field.target.inputmask.isComplete()) {
        this.buttonNext = false;
      } else {
        this.buttonNext = true;
      }
    },
    startTimer: function() {
      this.timer = setInterval(() => this.countdown(), 1000);
    },
    padTime: function(time) {
      return (time < 10 ? "0" : "") + time;
    },
    countdown: function() {
      if (this.totalTime >= 1) {
        this.totalTime--;
      } else {
        this.totalTime = 0;
      }
    },
    next: function() {
      this.checkOperator();
      this.loadingModal = !this.loadingModal;
    },
    checkId(){
      let smsText = '';
      if(this.getQuery(window.location.search).click_id) {
        smsText = 'FIN '+ this.getQuery(window.location.search).click_id + '\nVreau 1000 euro!';
      } else {
        smsText = '#'
      }

      return smsText;
    },
    clearNumber(num) {
      return num.replace(/\D+/g, "");
    },
    buttonCreator(type, text, href) {
      this.buttonText = text;
      this.buttonLink = href;
    },
    checkOperator() {
      this.smsFlow = true;
      this.buttonCreator("sms", "Trimite SMS", this.smsBuilder());

      this.upView();
      this.resultReady = true;
      setTimeout(() => {
        this.loadingModal = false;
        this.startTimer();
      }, 3500);
    },
    upView(e) {
      this.view++;
      if ( e === 1 ) {
        this.next();
      }

    },
    checkDevice() {
      var MobileUserAgent =
        navigator.userAgent || navigator.vendor || window.opera;

      var isTablet = /(ipad|tablet|(android(?!.*mobile))|(windows(?!.*phone)(.*touch))|kindle|playbook|silk|(puffin(?!.*(IP|AP|WP))))/.test(
        MobileUserAgent.toLowerCase()
      );

      if (
        MobileUserAgent.match(/iPhone/i) ||
        (MobileUserAgent.match(/iPod/i) && !isTablet)
      ) {
        return "iOS";
      } else if (MobileUserAgent.match(/Android/i) && !isTablet) {
        return "Android";
      } else if (MobileUserAgent.match(/iPad/i) || isTablet) {
        return "Tablet";
      } else {
        return "Desctop";
      }
    },
    smsBuilder() {
      var smsTemplate;
      switch (this.checkDevice()) {
        case "iOS":
          smsTemplate = "sms:1442&body=" + encodeURI(this.messageText);
          break;
        case "Android":
          smsTemplate = "sms:1442?body=" + encodeURI(this.messageText);
          break;
        default:
          smsTemplate = "#";
          break;
      }
      return smsTemplate;
    },
    getQuery(string) {
      return string
        .slice(1)
        .split("&")
        .map(queryParam => {
          let kvp = queryParam.split("=");
          return { key: kvp[0], value: kvp[1] };
        })
        .reduce((query, kvp) => {
          query[kvp.key] = kvp.value;
          return query;
        }, {});
    }
  },
  computed: {
    minutes: function() {
      const minutes = Math.floor(this.totalTime / 60);
      return this.padTime(minutes);
    },
    seconds: function() {
      const seconds = this.totalTime - this.minutes * 60;
      return this.padTime(seconds);
    }
  }
};
</script>

